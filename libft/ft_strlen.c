#include "libft.h"

size_t	ft_strlen(const char *str)
{
	size_t	i;
	int res;

	i = 0;
	res = 0;
	if (str[i] == '-')
		res--;
	while (str[i])
	{
		res++;
		i++;
	}
	return (res);
}
